// grants.json
// ============================================================================
{
  const vectors = [];
  let gid = 1;
  let cn = 0x60;

  const NOW = T0 + 30800; // 2026-09-10 (Thu) 09:00:00 UTC
  const SUNDAY = utc(2026, 9, 13, 10, 0); // Sunday 10:00:00 UTC

  const grantBody = (over = {}) => ({
    v: 0,
    typ: 'grant',
    grant_id: uuidN('9aa70000', gid++),
    member: MEMBER_ID,
    app_pubkey: KEYS.app.pubB64u,
    devices: [DEVICE_ID],
    access_points: ['main', 'pedestrian'],
    windows: [{ days: 'mon-sun', from: '00:00', to: '24:00' }],
    iat: T0 - 86400,
    exp: T0 - 86400 + GRANT_TTL,
    ...over,
  });

  /**
   * Build a full transcript vector. `opts`:
   *   grantOver     — grant field overrides
   *   grantSigner   — default 'gateway'
   *   proofSigner   — default 'app'
   *   proofOver     — proof field overrides
   *   accessPoint   — requested access point (open + proof), default 'main'
   *   now           — controller clock at redemption
   *   lastSync      — last gateway clock sync
   *   lockdown
   */
  function transcriptVec(name, desc, expectation, opts = {}) {
    const now = opts.now ?? NOW;
    const ap = opts.accessPoint ?? 'main';
    const grant = signedVec(opts.grantSigner ?? 'gateway', grantBody(opts.grantOver));
    const challenge = {
      v: 0,
      typ: 'grant.challenge',
      cnonce: nonce(cn++),
      iat: now - 2,
      exp: now - 2 + CNONCE_TTL,
    };
    const proofBody = {
      v: 0,
      typ: 'grant.proof',
      grant_id: grant.object.grant_id,
      cnonce: challenge.cnonce,
      access_point: opts.proofOver?.access_point ?? ap,
      ts: now - 1,
      ...opts.proofOver,
    };
    const proof = signedVec(opts.proofSigner ?? 'app', proofBody);
    const open = unsignedVec({ v: 0, typ: 'grant.open', grant: grant.object, access_point: ap });
    return {
      name,
      desc,
      ...expectation,
      check: {
        now,
        device_id: DEVICE_ID,
        last_gateway_sync: opts.lastSync ?? now - 3600,
        lockdown: opts.lockdown ?? false,
      },
      grant,
      transcript: { open, challenge, proof },
    };
  }

  vectors.push(
    transcriptVec(
      'grant-redeem-valid',
      'Full happy-path offline redemption: fresh grant, live cnonce, app-signed proof.',
      { expect: 'accept' }
    )
  );
  vectors.push(
    transcriptVec('grant-stale-clock', 'Controller offline (no gateway clock sync) > 14 days: refuse redemption entirely.', {
      expect: 'reject',
      reason: 'stale_clock',
    }, { lastSync: NOW - STALE_CLOCK_LIMIT - 1 })
  );
  vectors.push(
    transcriptVec('grant-lockdown', 'Offline grants are refused during lockdown.', {
      expect: 'reject',
      reason: 'lockdown',
    }, { lockdown: true })
  );
  vectors.push(
    transcriptVec('grant-badsig', 'Grant signed by a key that is not the pinned gateway key.', {
      expect: 'reject',
      reason: 'badsig',
    }, { grantSigner: 'attacker' })
  );
  vectors.push(
    transcriptVec('grant-expired', 'Grant exp + 90 s is in the past.', {
      expect: 'reject',
      reason: 'expired',
    }, { grantOver: { iat: T0 - 2 * GRANT_TTL, exp: T0 - GRANT_TTL } })
  );
  vectors.push(
    transcriptVec('grant-wrong-device', 'This controller is not in grant.devices.', {
      expect: 'reject',
      reason: 'wrong_device',
    }, { grantOver: { devices: [OTHER_DEVICE_ID] } })
  );
  vectors.push(
    transcriptVec('grant-wrong-access-point', 'Requested access point is not in grant.access_points.', {
      expect: 'reject',
      reason: 'wrong_access_point',
    }, { accessPoint: 'vehicle' })
  );
  vectors.push(
    transcriptVec(
      'grant-window-denied',
      'Grant limited to mon-fri 08:00-17:00 (UTC); redemption attempted Sunday 10:00 UTC.',
      { expect: 'reject', reason: 'window' },
      {
        grantOver: { windows: [{ days: 'mon-fri', from: '08:00', to: '17:00' }] },
        now: SUNDAY,
      }
    )
  );
  vectors.push(
    transcriptVec(
      'grant-window-accepted',
      'Same mon-fri 08:00-17:00 window, redeemed Thursday 09:00 UTC: inside.',
      { expect: 'accept' },
      { grantOver: { windows: [{ days: 'mon-fri', from: '08:00', to: '17:00' }] } }
    )
  );
  vectors.push(
    transcriptVec('grant-proof-badsig', 'Proof signed by a key other than grant.app_pubkey.', {
      expect: 'reject',
      reason: 'badsig',
    }, { proofSigner: 'attacker' })
  );
  vectors.push(
    transcriptVec('grant-proof-wrong-grant', 'proof.grant_id does not match the presented grant.', {
      expect: 'reject',
      reason: 'wrong_grant',
    }, { proofOver: { grant_id: uuidN('9aa70000', 999) } })
  );
  vectors.push(
    transcriptVec('grant-cnonce-expired', 'Proof arrives after the 30 s challenge validity.', {
      expect: 'reject',
      reason: 'cnonce_expired',
    }, {
      // challenge is issued at now-2 with exp now+28; shift proof+clock past it:
      proofOver: { ts: NOW + 40 },
    })
  );
  // fix up the cnonce-expired vector: verification clock must be past challenge.exp
  vectors[vectors.length - 1].check.now = NOW + 40;
  vectors.push(
    transcriptVec('grant-proof-stale-ts', 'proof.ts more than 90 s behind the controller clock.', {
      expect: 'reject',
      reason: 'expired',
    }, { proofOver: { ts: NOW - 200 } })
  );

  // cnonce replay: identical proof presented twice against one challenge
  {
    const base = transcriptVec('grant-cnonce-replay', '', { expect: 'accept' });
    vectors.push({
      name: 'grant-cnonce-replay',
      desc: 'Same proof presented twice for one issued cnonce; second attempt rejected.',
      check: base.check,
      grant: base.grant,
      transcript: { open: base.transcript.open, challenge: base.transcript.challenge },
      steps: [
        { proof: base.transcript.proof, expect: 'accept' },
        { proof: base.transcript.proof, expect: 'reject', reason: 'cnonce_replay' },
      ],
    });
  }

  writeVectors('grants.json', { ...HEADER('grants.md'), vectors });
}

