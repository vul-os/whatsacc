#!/usr/bin/env node
// proto/vectors/generate.mjs — deterministic conformance-vector generator for the
// whatsacc v0 wire contracts (../pairing.md ../commands.md ../grants.md ../events.md).
//
// Fully deterministic: fixed seeds (lib.mjs), fixed timestamps, fixed nonces, and
// Ed25519 signing is itself deterministic (RFC 8032) — running this twice produces
// byte-identical JSON. Run: node proto/vectors/generate.mjs
//
// TEST KEYS ONLY — see the banner in lib.mjs.

import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { KEYS, SEEDS, b64u, jcs, signObject } from './lib.mjs';

const OUT = dirname(fileURLToPath(import.meta.url));

// --- Fixed constants ---------------------------------------------------------

const T0 = 1789000000; // 2026-09-10T00:26:40Z — a Thursday (matches spec examples)
const SKEW = 90; // seconds, both bounds
const MAX_CMD_WINDOW = 60; // exp - iat ceiling
const CNONCE_TTL = 30; // grant.challenge validity
const STALE_CLOCK_LIMIT = 1209600; // 2 × default grant TTL (14 days), fixed in v0
const GRANT_TTL = 604800; // 7 days

const DEVICE_ID = 'de71ce00-0000-4000-8000-000000000001';
const OTHER_DEVICE_ID = 'de71ce00-0000-4000-8000-000000000002';
const MEMBER_ID = '3e3be500-0000-4000-8000-000000000001';
const CHAT_EVENT_ID = 'c4a70000-0000-4000-8000-000000000001';
const ACCESS_POINTS = ['main', 'pedestrian']; // what this controller serves

// Deterministic 128-bit nonces: 16 bytes of a repeated byte value.
const nonce = (n) => b64u(Buffer.alloc(16, n));
const uuidN = (prefix, n) =>
  `${prefix}-0000-4000-8000-${String(n).padStart(12, '0')}`;

const utc = (y, mo, d, h, mi, s = 0) => Date.UTC(y, mo - 1, d, h, mi, s) / 1000;

// --- Helpers -----------------------------------------------------------------

/** Build a signed-vector entry: wire object + canonical bytes + signer name. */
function signedVec(signer, obj) {
  const { object, canonical } = signObject(obj, KEYS[signer]);
  return { signer, object, canonical };
}

/** Signed by `signer`, then fields mutated AFTER signing (tamper case). */
function tamperedVec(signer, obj, mutate) {
  const { object } = signObject(obj, KEYS[signer]);
  const t = { ...object };
  mutate(t);
  const { sig, ...rest } = t;
  return { signer: null, tampered_by: 'mutation after signing', object: t, canonical: jcs(rest) };
}

function unsignedVec(obj) {
  return { signer: null, unsigned: true, object: obj, canonical: jcs(obj) };
}

function writeVectors(file, doc) {
  writeFileSync(join(OUT, file), JSON.stringify(doc, null, 2) + '\n');
  console.log('wrote', file, `(${doc.vectors.length} vectors)`);
}

const HEADER = (contract) => ({
  $comment:
    'AUTOGENERATED by generate.mjs — do not edit by hand. ' +
    'TEST KEYS ONLY: all keys are public deterministic test constants; never use in production.',
  contract,
  version: 0,
  spec_constants: {
    skew_seconds: SKEW,
    max_cmd_window_seconds: MAX_CMD_WINDOW,
    cnonce_ttl_seconds: CNONCE_TTL,
    stale_clock_limit_seconds: STALE_CLOCK_LIMIT,
  },
});

